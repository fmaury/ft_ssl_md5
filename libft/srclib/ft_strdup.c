#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*cpy;

	i = 0;
	if (!(cpy = (char*)malloc(sizeof(char) * ft_strlen(src) + 1)))
		return (NULL);
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
